/*
The pop() method removes the last element from an array.
The pop() method returns the value that was "popped out":
Altera o array
 */

var fruits = ["Banana", "Orange", "Apple", "Mango"];
console.log(fruits);
var fruitRemoved = fruits.pop(); // Removes the last element ("Mango") from fruits 
console.log(fruits);
console.log(fruitRemoved); // Mango
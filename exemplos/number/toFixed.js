/*
https://www.w3schools.com/jsref/jsref_tofixed.asp

The toFixed() method converts a number into a string,
keeping a specified number of decimals.
Note: if the desired number of decimals are higher than the actual
number, nulls are added to create the desired decimal length.

Syntax: number.toFixed(x)

Parameter Values:
x: Optional. The number of digits after the decimal
point. Default is 0 (no digits after the decimal point)
*/

console.log(5.56789.toFixed(2)) //"5.57"







